    private boolean checkvalidation(){      
        
        if(txtStudentID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "employee ID cannot be blank");
            return false;
        }
        
        try{
            
            int sID=Integer.valueOf(txtStudentID.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "employee Id must be numeric");
            return false;
        }
        
        
        if(txtFirstName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "First Name cannot be blank");
            return false;
        }
        
        if(txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Last Name cannot be blank");
            return false;
        }
        
        if(txtDOB.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Date of Birth cannot be blank");
            return false;
        }
        
        try{
            Date dob=Date.valueOf(txtDOB.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Date must be YYYY-MM-DD format");
            return false;
        }
        
         return true;
    }
    

