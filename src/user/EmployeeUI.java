    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
       if(checkvalidation()){
            int sID=Integer.valueOf(txtStudentID.getText());
        String firstName=txtFirstName.getText();
        String lastName=txtLastName.getText();
        String gender="";
        if(rdoMale.isSelected()){
            gender="Male";
        }else if(rdoFemale.isSelected()){
            gender="Female";
        }else{
            gender="";
        }
        String designations=String.valueOf(cmbCourse.getSelectedItem());
        Date dob=Date.valueOf(txtDOB.getText());
        
        String departments=String.valueOf(jComboBox1.getSelectedItem());
       
        
        employee s=new employee(sID, firstName, lastName, gender, designations,departments,dob);
        boolean result=sDB.add(s);
        if(result){
             JOptionPane.showMessageDialog(this, "New employee record is added");
        }else{
            JOptionPane.showMessageDialog(this, "New employee record is not added");
        }
        
        txtStudentID.setText("");
        txtFirstName.setText("");
        txtLastName.setText("");
        rdoMale.setSelected(false);
        rdoFemale.setSelected(false);
        cmbCourse.setSelectedIndex(0);
        txtDOB.setText("");
        jComboBox1.setSelectedIndex(0);
       }
        
       
        
        
    }//GEN-LAST:event_btnOKActionPerformed

    private void btnGetAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetAllActionPerformed
        // TODO add your handling code here:
     // txtDisplay.setText("");
      ArrayList<employee> sList=sDB.getAll(); 
      tblModel.setRowCount(0);
      sList.forEach((s) -> {
          int sID=s.getsID();
          String firstName=s.getFirstName();
          String lastName=s.getLastName();
          String gender=s.getGender();
          String designations=s.getDesignations();
          String departments=s.getDepartments();
          Date dob=s.getDob();
          tblModel.addRow(new Object[]{sID,firstName,lastName,gender,designations,departments,dob});
          //  txtDisplay.append(line+"\n");
        });
    }//GEN-LAST:event_btnGetAllActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        int sID=Integer.valueOf(JOptionPane.showInputDialog("Enter employee ID:"));
        employee s=sDB.get(sID);
        if(s!=null){
            txtStudentID.setText(String.valueOf(s.getsID()));
            txtFirstName.setText(s.getFirstName());
            txtLastName.setText(s.getLastName());
            if(s.getGender().equals("Male")){
                rdoMale.setSelected(true);
            }else{
                rdoFemale.setSelected(true);
            }
            cmbCourse.setSelectedItem(s.getDesignations());
            txtDOB.setText(String.valueOf(s.getDob()));
            jComboBox1.setSelectedItem(s.getDepartments());
        }else{
            JOptionPane.showMessageDialog(this, "The record is not found");
        }
    }//GEN-LAST:event_btnFindActionPerformed

